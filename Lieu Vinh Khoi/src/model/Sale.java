package model;

import java.io.Serializable;

/**
 *
 * @author Liêu Vinh Khôi
 */
public class Sale implements Serializable{
    private int id;
    private String name;
    private String snack;
    private String date;
    private int total;

    public Sale(int id, String name, String snack, String date, int total) {
        this.id = id;
        this.name = name;
        this.snack = snack;
        this.date = date;
        this.total = total;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSnack() {
        return snack;
    }

    public void setSnack(String snack) {
        this.snack = snack;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
